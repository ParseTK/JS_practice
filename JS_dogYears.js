// Current age 
let myAge = 30;  

// Initial years for a dog
 let earlyYears = 2; 

 // Calculate dog years for early years 
 earlyYears *= 10.5; 

 // Years beyond the initial two 
 let laterYears = myAge - 2; 

 // Calculate dog years for later years 
 laterYears *= 4; 

 // Total age in dog years 
 let myAgeInDogYears = earlyYears + laterYears; 

 // Name in lowercase 
 let myName = 'Tyler'.toLowerCase(); 

 // Result 
 console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);
